@Composable
fun PinVerifyButton(
    pin: String,
    onVerifyPin: () -> Unit,
    isLoading: Boolean
) {
    Button(
        onClick = onVerifyPin,
        enabled = pin.length == 4 && !isLoading,
        modifier = Modifier.fillMaxWidth()
    ) {
        if (isLoading) {
            CircularProgressIndicator(
                modifier = Modifier.size(24.dp),
                color = MaterialTheme.colorScheme.onPrimary
            )
        } else {
            Text("Verify PIN")
        }
    }
}
